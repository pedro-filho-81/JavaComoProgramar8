// classe Texto com printif
public class TextoComPrintf {

    // função principal
    public static void main(String[] args) {
        // imprime formatado
        // %s exebe uma string
        // %n pula linha
        System.out.printf("%n%s%n%n%s%n", "Bem vindo para:", "Programação Java!" );
    } // fim main
} // fim classe
